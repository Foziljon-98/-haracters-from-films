// Handle like click
function handleLike(id){
    const newDB = dbFromLocal.map(item => {
        if(item.id === id){
            if(item.isLiked === null){
                return {
                    ...item,
                    isLiked: true
                }
            }else{
                return {
                    ...item,
                    isLiked: !item.isLiked
                }
            }
        }else{
            return item
        }
    })
    localStorage.setItem('database', JSON.stringify(newDB));
    window.location.reload();
}



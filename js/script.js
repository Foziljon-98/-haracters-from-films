const dbFromLocal = JSON.parse(localStorage.getItem('database'));

// Показать карточки при открытии страницы
window.addEventListener('load', showCards(dbFromLocal));






// Функция которая обрабатывает все карточки и показывает на странице

function showCards(arr){
    const cards = arr.map(({names, species, gender, faculty, actor, image, id, isLiked}) => {
        return `
            <div class="col-lg-3 col-md-4 col-sm-6 mb-4">
                <div class="card">
                 <img src="${image}" alt="">
                    <div class="descriptions">
                        <h4 class="card-title text-center text-light"><a>${names}</a></h4>
                        <ul>
                            <li>Species: ${species}</li>
                            <li>Gender:  ${gender}</li>
                            <li>Faculty:  ${faculty}</li>
                            <li>Actor:  ${actor}</li>
                        </ul>
                        
                        <div class="likedHeart">
                            <button onclick="showSingle(${id})">
                            Подробнее
                            </button>
                            <i onclick="handleLike(${id})" class="fas fa-heart" style="${isLiked ? 'color: red' : isLiked === null ? 'color: #ccc' : 'color: #000'}"></i>
                        </div>
                        
                    </div>
                </div>

                
            </div>
        `
    }).join('');

    container.innerHTML = cards;
}


 


function showSingle(id){
    const filterArr = dbFromLocal.filter(item => item.id === id);
    localStorage.setItem('singleCharacter' , JSON.stringify(filterArr));
    window.open('single.html' , '_self');
}




// Search

nameInput.addEventListener('input', e => {
    const searchString = e.target.value.toUpperCase();

    facultyInput.value = "";

    const filteredArr = database.filter(({names}) => names.toUpperCase().includes(searchString));
    showCards(filteredArr);
})

facultyInput.addEventListener('input', e => {
    const searchString = e.target.value.toUpperCase();
    
    nameInput.value = "";

    const filteredArr = database.filter(({faculty}) => faculty.toUpperCase().includes(searchString));
    showCards(filteredArr);
})







